import service.memberService.memberModifyService;

/**
 * Servlet implementation class memberDeleteController
 */
@WebServlet("/memberDeletePage")
public class memberDeletePageController extends HttpServlet {
	private static final long serialVersionUID = 1L;
    
	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		doProcess(request, response);
	}

	protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		doProcess(request, response);
	}
	
	protected void doProcess(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		request.setCharacterEncoding("UTF-8");
		response.setContentType("text/html; charset=utf-8");
		
		RequestDispatcher dispatcher = request.getRequestDispatcher("member/memberDelete.jsp");
		dispatcher.forward(request, response);
	}

}
